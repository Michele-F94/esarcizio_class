public class arrayDiStringhe {
    public static void main(String[] args) {
        
        String[] arrayDiStringhe = {"Anna", "Luca", "Alberto", "Giulia", "Andrea"}; // Array di stringhe

        // Stampa ogni stringa dell'array
        for (int i = 0; i < arrayDiStringhe.length; i++) {
            if (arrayDiStringhe[i].charAt(0) == 'A') {

                System.out.println(arrayDiStringhe[i]); // Stampa la stringa corrente se in
                
            }
           
        }
    }

}
